package com.example.entity;

public class Player {
    private Integer playerId;
    private String playerName;
    private Integer playerHighScore;
    private Integer playerLevel;
    private Integer playerNationalId;
    private String PlayerNationalName;

    
    public Integer getPlayerId() {
        return playerId;
    }
    public void setPlayerId(Integer playerId) {
        this.playerId = playerId;
    }
    public String getPlayerName() {
        return playerName;
    }
    public void setPlayerName(String playerName) {
        this.playerName = playerName;
    }
    public Integer getPlayerHighScore() {
        return playerHighScore;
    }
    public void setPlayerHighScore(Integer playerHighScore) {
        this.playerHighScore = playerHighScore;
    }
    public Integer getPlayerLevel() {
        return playerLevel;
    }
    public void setPlayerLevel(Integer playerLevel) {
        this.playerLevel = playerLevel;
    }
    public Integer getPlayerNationalId() {
        return playerNationalId;
    }
    public void setPlayerNationalId(Integer playerNationalId) {
        this.playerNationalId = playerNationalId;
    }
    public String getPlayerNationalName() {
        return PlayerNationalName;
    }
    public void setPlayerNationalName(String playerNationalName) {
        PlayerNationalName = playerNationalName;
    }

    

   

    
}
